Byte * IndexDataToCache(Byte *data) {
    int model = data[0];
    int serverCurrent = data[1];
    Byte bitCustom = data[2];
    int online = data[3];
    if (!model) return data + online;
    for (int i = online; i < online + serverCurrent; i++) {
        int value = data[i] + bitCustom;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[online + serverCurrent] = 0;
    return data + online;
}

NSString *StringFromIndexData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)IndexDataToCache(data)];
}
